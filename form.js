function apri(cosa)
{
 window.open(cosa,"Legenda","toolbar=no,location=no,directories=no,status=no,scrollbars=yes,resizable=no,history=no,width=300,height=400");
}

function ToggleCheckart()
{
	var sa = false;
	if (document.art.CheckAll.checked) sa = true;
	for (var i = 0; i < document.art.elements.length; i++) {
		var e = document.art.elements[i]
		if (sa) e.checked = true
		else e.checked = false
	}
}
